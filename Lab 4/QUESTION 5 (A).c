
#include <math.h>

int main()
{   //CEIL FUNCTION
    printf("CEIL FUNCTION: %f",ceil (25.38));
    //FLOOR FUNCTION
    printf("\n FLOOR FUNCTION: %f",floor (25.38));
    //Square root
    printf("\nSQUARE ROOT FUNCTION: %f",sqrt (25.38));
    // power FUNCTION
    printf("\n POWER FUNCTION: %f",pow (25.38,2));
    //ABSOLUTE FUNCTION
    printf("\n ABSOLUTE FUNCTION: %f",abs (25.38));
     return 0;
}
